class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        // Your code here
        int max=INT_MIN;
        int maxe=0;
        for(int i=0;i<n;i++){
            maxe=maxe+arr[i];
            if(max<maxe){
                max=maxe;
            }
            if(maxe<0){
                maxe=0;
            }
        }
        return max;
    }
};

